package com.bola.nwcl.api.model;

import com.wordnik.swagger.annotations.ApiModelProperty;

public class ArticleReleaseImgModel {
	private Long id;
	private String imgPath;
	private String imgPathThumbnail;
	
	@ApiModelProperty(value = "放行条图片id")
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	@ApiModelProperty(value = "放行条图片路径")
	public String getImgPath() {
		return imgPath;
	}
	public void setImgPath(String imgPath) {
		this.imgPath = imgPath;
	}
	@ApiModelProperty(value = "放行条缩略图片路径")
	public String getImgPathThumbnail() {
		return imgPathThumbnail;
	}
	public void setImgPathThumbnail(String imgPathThumbnail) {
		this.imgPathThumbnail = imgPathThumbnail;
	}
	
	
}
